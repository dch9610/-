1.벡터
- 같은 유형의 데이터를 여러개 변수로 보관해야할 경우
- 여러 변수들을 하나의 이름으로 묶은 집합
- c () 함수를 이용하여 벡터 생성
coffee<-c(3000,4000,5000)


2.벡터 항목에 이름 설정하기
- names()함수를 이용하여 이름 설정
- names(이름을 저장할 객체) <- 이름값 지정

names(coffee)<-c("아메리카노","에스프레소","카페라떼")
coffee

3.벡터에 접근하기
coffee[1]
coffee[3]
coffee[2:3]
coffee[-3]
